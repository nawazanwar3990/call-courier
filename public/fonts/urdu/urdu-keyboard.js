function typeUrdu(cElement) {
    var s = $(cElement).val();
    var S = [];
    for (var x = 0; x < s.length; x++) {
        var c = s.charAt(x);
        S.push(m[c] || c);
    }
    $(cElement).val(S.join(''));
}
